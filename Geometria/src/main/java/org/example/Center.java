package org.example;

public class Center {
    private double x ;
    private double y ;

    public Center(double x, double y) {
        this.setX(x);
        this.setX(y);
    }

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }
}
